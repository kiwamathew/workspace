# Syntax about variable names in python
# A name given to a partition through programming is a variable.
num1 = 65 # num1 is a variable
num2 = 100 # num2 is a variable
print(num1)
print(num1 + num2)
# The name of a variable should not start with a number or numeral eg 1num, 2num
# A varaiable name should not start with a special character eg #num, @num, !num, $num
# Compound varaiable names should not have spaces eg num 1, num 2, my num
# Compound variable names should be joined by either an underscore or camel case/camel notation eg my_num, num_1, myNum
# Avoid using capital letters to begin the name of the variable eg Num1, Num2
# Do not use special words as variable names eg print, else, if, while, switch, def, elif
# Varaibles in python are case sensitive eg Num1 is different from num1
